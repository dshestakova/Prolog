/*Changing list item*/

repl(_,_,[],[]):-!.   %Случай, когда список пустой 
repl(A,B,[A|Tail],[B|Tail_]):-!,repl(A,B,Tail,Tail_). %Если А в голове списка, то сразу заменяем на В и запускаем рекурсией дальше, откидывая голову; 
repl(A,B,[X|Tail],[X|Tail_]):-repl(A,B,Tail,Tail_).   %Если нет, то отсекаем голову и рассматриваем оставшийся список
