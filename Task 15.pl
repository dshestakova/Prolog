

insert([],Q,Q).			%Случай с пустым
insert([H|T],Q,A):-		%Помещаем отсортированную часть в аккумулятор
    just(H,Q,Tmp),
    insert(T,Tmp,A).
   
just(X,[],[X]).				%Если в списке один *штука*
just(X,[Y|T],[Y|T1]):-		%если X больше, то переставляем и запускам к следующему элементу
    X>Y,
    just(X,T,T1).
just(X,[Y|T],[X,Y|T]):-		%Если нет, то все хорошо
    X=<Y.

start([],[]).			%Случай с пустым списком
start(Q,A):-			%Стартовый предикат
    insert(Q,[],A).


